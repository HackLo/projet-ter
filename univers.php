Le dôme

À l'intérieur du dôme, une communauté vivant en autarcie s'était préparée à la catastrophe, offrant à ses habitants une solution sage et viable pour leur survie sur le long 

terme. Le Dôme les protège, tandis qu'un soleil synthétique, organe vital de la structure, les alimente en énergie et leur offre un écosystème florissant que le reste du monde 

croyait à jamais disparu. Le peuple est cultivé, éduqué aux pratiques respectueuses de son environnement : développement durable, intégration adéquate de la technologie dans le 

quotidien et maîtrise de la consommation d'énergie définissent leur mode de vie. C'est Ed Gaïa, un vieil homme parmi d'autres, qui se charge d'initier Raynor à ce nouveau monde.


