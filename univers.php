    <h2 class="soustitre">La planète Terre</h2>
	

<p>Notre planète est un amoncellement de cendres sans âme dans lequel le vivant n'a plus vraiment son mot à dire. Seule espèce capable d'affronter les conséquences d'une explosion volcanique dévastatrice, l'Humain a choisi de profondément modifier son métabolisme pour amoindrir les dangers et douleurs imposés par cet environnement, qui est désormais son 

principal prédateur. Contraint de se prémunir pour survivre, il se retrouve esclave des technologies transhumanistes dont il rêvait autrefois et le plaisir simple de la 

nourriture a disparu depuis bien longtemps de son mode de vie. Le minerais d'uranium est la principale source d'énergie de ce nouveau monde, et comme toute ressource naturelle, 

son exploitation est synonyme de son épuisement. L'ennemi commun a changé, et l'armée s'est adaptée : c'est elle qui explore les terres environnantes à la recherche de nouveaux 

gisements. Raynor Hell est l'un des leaders à la tête des expéditions dans l'inconnu.

