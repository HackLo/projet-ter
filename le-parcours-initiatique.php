    <h1>Le parcours initiatique</h1>
    
	<p>Quantité : 2 personnes<br />
    Temps de préparation : 2 mois<br />
	Niveau de difficulté : Moyen</p>
    
    <h2>Ingrédients</h2>
	<p>2 êtres humains<br />
	150g de réticence<br />
	2kg de curiosité<br />
	1 pincée d'appétit</p>

	<h2>Préparation</h2>
	<p>Prenez deux mondes avec une culture différente.<br />
        Ajoutez-y un infiltré.<br />
        Faites mariner un soupçon d'étonnement et de réticence face à un petit déjeuner.<br />
        Ensuite, incorporez à l'incompréhension une pincée d'attirance à la vue d'un livre de cuisine.<br />
        Préchauffez votre appétit et goûtez enfin à la préparation.<br />
        Une fois l'intérêt grandissant, faites fondre les craintes et enfournez la curiosité. Simultanément, assaisonnez la préparation avec de nouveaux produits.<br />
        Dans un saladier, versez la réalisation et saupoudrez de curiosité avec l'aide d'un ami.<br />
        Laissez reposer.<br />
        Lorsque l'enseignement devient solide, réchauffez l'ensemble et ajoutez-y un ingrédient mystère.<br /><br />
        
        Mélangez le tout et obtenez un nouvel amateur de gastronomie !</p>
    
