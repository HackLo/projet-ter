	<h1>Inspirations</h1>
  
	<div class="blocleft">
		<h2>Inception</h2>
		<p>Film de Christopher Nolan</p>
		<a href="http://www.allocine.fr/video/player_gen_cmedia=19114615&cfilm=143692.html" target="_blank"><img src="graphics/inspiration-inception.jpg" class="imagecontenu" width="190" alt="Inception" /></a>
	</div>
	<div class="blocright">
		<p>Dans un futur proche, l'armée américaine a développé le "rêve partagé", une méthode permettant d'influencer l'inconscient d'une victime pendant qu'elle rêve. Ainsi, on découvre la ville des "limbes" dont nous nous sommes inspirés pour le monde à l'extérieur du dôme. Les limbes, sorte de dimension de rêve parallèle, sont l'un des éléments les plus abstraits et mystérieux du film.</p>
	</div>
	<div style="clear: both;"></div>


	<div class="blocleft">
		<h2>Under the dome</h2>
		<p>Série TV de Brian K. Vaughan,<br /> 
        d'après le roman de Stephen King</p>
		<a href="http://www.allocine.fr/video/player_gen_cmedia=19513237&cserie=7834.html" target="_blank"><img src="graphics/inspiration-underthedome.jpg" class="imagecontenu" width="190" alt="Under the Dome" /></a>
	</div>
	<div class="blocright">
		<p>Les habitants de la petite communauté de Chester's Mill se réveillent un matin coupés du monde et piégés dans la ville par un immense dôme transparent. Certains tenteront de tirer profit de cette situation inexpliquée afin de prendre le pouvoir. Mais une résistance va s'organiser  pour empêcher ces personnes de parvenir à leur fin. Ce dôme est à l'origine même de l'idée des deux univers de cette nouvelle.</p>
	</div>
	<div style="clear: both;"></div>
    
    <div class="blocleft">
		<h2>Bio-Dome</h2>
		<p>Film de Jason Bloom</p>
		<a href="http://www.youtube.com/watch?v=4EWikCCfHJw" target="_blank"><img src="graphics/inspiration-biodome.jpg" class="imagecontenu" width="190" alt="Bio-Dome" /></a>
	</div>
	<div class="blocright">
		<p>Deux amis se retrouvent accidentellement enfermés dans un complexe scientifique appelé "Bio-Dôme". Ce dernier, situé dans le désert de l'Arizona, consiste en une expérience écologique d'une durée d'un an. Cette comédie tourne autour d'une question : Bud et Doyle seront-ils capables de s'adapter à ce nouvel environnement ? Ce film est une autre inspiration de l'idée des deux univers et du dôme.</p>       
	</div>
	<div style="clear: both;"></div>

	<div class="blocleft">
		<h2>Living Tomorrow</h2>
		<p>Projet de Michael van Oosten</p>
		<a href="http://livingtomorrow.com/fr/" target="_blank"><img src="graphics/inspiration-livingtomorrow.jpg" class="imagecontenu" width="190" alt="Living Tomorrow" /></a>
	</div>
	<div class="blocright">
		<p>Living Tomorrow est un lieu de rencontres pour les entreprises novatrices. Il leur permet de faire connaître aux visiteurs des produits et services qui pourraient améliorer la qualité de vie et de travail, dans un futur proche. Ce projet a inspiré notre vision de la maison d'Ed Gaïa, notamment la maison de Vilvorde pour l'espace intérieur et la cuisine. Il est possible de visiter cette maison à travers cette <a href="http://www.youtube.com/watch?v=9DJr8QwgLEA" target="_blank">vidéo</a>.</p>
	</div>
	<div style="clear: both;"></div>
    
    <div class="blocleft">
		<h2>Nanotechnologie</h2>
		<p>La cuisine du futur</p>
		<a href="http://www.youtube.com/watch?v=ZrmYdMZaMwY" target="_blank"><img src="graphics/inspiration-livingkitchen.jpg" class="imagecontenu" width="190" alt="Living Kitchen" /></a>
	</div>
	<div class="blocright">
		<p>Imaginez la cuisine du futur pouvant se transformer à volonté grâce à un mur fait de nanotechnologies. Ainsi, il serait possible de tirer une surface offrant de multiples possibilités : d'un coup avec le poing, vous créez une cavité dans la surface pour cuisiner votre repas, ou encore selon les gestes, différentes formes prédéfinies permettent d'aménager instantanément et intuitivement sa cuisine selon les besoins.</p>
	</div>
	<div style="clear: both;"></div>
    
    <div class="blocleft">
		<h2>Philips Biosphère</h2>
		<p>Home Farming</p>
		<a href="http://www.trendsnow.net/2009/08/philips-biosphere-home-farming.html" target="_blank"><img src="graphics/inspiration-biosphere.jpg" class="imagecontenu" width="190" alt="Philips Biosphère" /></a>
	</div>
	<div class="blocright">
		<p>Ce concept de cuisine futuriste imaginée par Philips est une mini-ferme écologique et auto-suffisante, servant à produire une partie de l'alimentation d'une famille. Mettant en synergie les poissons, les légumes, les plantes et les algues, cette biosphère verticale générera ainsi de manière entièrement naturelle de la nourriture, de l'eau filtrée et du gaz domestique, en s'alimentant notamment par les déchets de la cuisine.</p>
	</div>
	<div style="clear: both;"></div>

	<div class="blocleft">
		<h2>Recette de cuisine</h2>
		<p>Velouté de châtaignes au foie gras</p>
		<a href="http://www.marmiton.org/recettes/recette_veloute-de-chataignes-au-foie-gras_46124.aspx" target="_blank"><img src="graphics/inspiration-veloute-chataignes.jpg" class="imagecontenu" width="190" alt="Recette velouté de châtaignes au foie gras" /></a>
	</div>
	<div class="blocright">
		<p>La nouvelle présente une délicieuse recette de cuisine traditionnelle : le velouté de châtaignes au foie gras. Recette de fête, cette préparation peut être servie avec de fines tranches de foie gras ou des morceaux de lard croustillants. Le goût présent et très agréable de la châtaigne se marie très bien avec le foie gras. Pour les amateurs de cuisine, la recette est disponible <a href="http://www.marmiton.org/recettes/recette_veloute-de-chataignes-au-foie-gras_46124.aspx" target="_blank">ici</a>.</p>
	</div>

